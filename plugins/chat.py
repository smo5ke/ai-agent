# plugins/chat.py

def run(param=None):
    # إذا كان هناك نص قادم من الدماغ (مثل hello)
    responses = {
        "hello": "أهلاً بك! أنا جارفيس، كيف يمكنني مساعدتك اليوم؟",
        "hi": "مرحباً! نظام الإضافات يعمل، أنا بانتظار أوامرك.",
        "status": "كل الأنظمة تعمل بكفاءة، الدماغ متصل والذاكرة نشطة."
    }
    
    # البحث عن رد مناسب، إذا لم يجد يرد برد افتراضي
    return responses.get(param.lower() if param else "hello", "مرحباً بك! أنا أسمعك.")